/**
 * 线性插值工具
 * @param {Array<[number,number]>} curve - [[x, y], ...] 已按 x 升序排列
 * @param {number} x
 * @returns {number|null}
 */
export function interpCurve(curve, x) {
  if (x < curve[0][0] || x > curve[curve.length - 1][0]) return null
  for (let i = 1; i < curve.length; i++) {
    if (x <= curve[i][0]) {
      const [x0, y0] = curve[i - 1]
      const [x1, y1] = curve[i]
      return y0 + (y1 - y0) * (x - x0) / (x1 - x0)
    }
  }
  return null
}

/**
 * 在指定流量下查询泵的运行参数（插值）
 * @param {object} pump   - PUMP_CATALOG 中的一项
 * @param {number} Q_ls   - 流量 [l/s]
 * @returns {{ H_m, eta_pct, P_kW, NPSH_m } | null}  null 表示超出范围
 */
export function pumpAtFlow(pump, Q_ls) {
  const H    = interpCurve(pump.curves.HQ,   Q_ls)
  const eta  = interpCurve(pump.curves.eta,  Q_ls)
  const P    = interpCurve(pump.curves.P,    Q_ls)
  const NPSH = interpCurve(pump.curves.NPSH, Q_ls)
  if (H === null) return null
  return { H_m: H, eta_pct: eta, P_kW: P, NPSH_m: NPSH }
}

/**
 * 从目录中找出能满足运行要求的泵，按效率降序排列
 * @param {number} Q_ls  - 所需流量 [l/s]
 * @param {number} H_m   - 所需扬程 [m]
 * @param {number} [tolerance=0] - 扬程容差比例，0.03 表示允许 -3%（ISO 9906 2B）
 * @returns {Array<{ pump, H_m, eta_pct, P_kW, NPSH_m }>}
 */
export function findMatchingPumps(Q_ls, H_m, tolerance = 0) {
  const results = []
  for (const pump of PUMP_CATALOG) {
    const op = pumpAtFlow(pump, Q_ls)
    if (!op) continue                                          // 超出 Q 范围
    if (op.H_m < H_m * (1 - tolerance)) continue              // 扬程不足（含容差）
    if (Q_ls < pump.operatingRange.Q_min_ls) continue         // 低于 Qmin
    results.push({ pump, ...op })
  }
  results.sort((a, b) => (b.eta_pct ?? 0) - (a.eta_pct ?? 0))
  return results
}

/**
 * Returns diagnostic info for each pump that failed to match,
 * explaining the reason.
 */
export function diagnosePumpMisses(Q_ls, H_m) {
  return PUMP_CATALOG.map(pump => {
    const op = pumpAtFlow(pump, Q_ls)
    if (!op) {
      return { pump, reason: 'q_out_of_range',
               detail: `Q=${Q_ls.toFixed(1)} l/s 不在曲线范围 [0, ${pump.operatingRange.Q_max_ls}] l/s` }
    }
    if (Q_ls < pump.operatingRange.Q_min_ls) {
      return { pump, reason: 'q_below_min',
               detail: `Q=${Q_ls.toFixed(1)} l/s < Q_min=${pump.operatingRange.Q_min_ls} l/s` }
    }
    // head insufficient
    const gap = H_m - op.H_m
    return { pump, reason: 'head_insufficient',
             detail: `泵扬程 ${op.H_m.toFixed(2)} m < 所需 ${H_m.toFixed(2)} m（差 ${gap.toFixed(2)} m）` }
  })
}
